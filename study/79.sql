SELECT 10+5, 10-5, 10*5, 10/5 FROM DUAL;

CREATE TABLE SAMPLE(
    COL1 INT,
    COL2 INT
);
INSERT INTO SAMPLE VALUES (10, 5);

SELECT * FROM SAMPLE;

SELECT COL1+COL2 AS A,
       COL1-COL2 AS S,
       COL1*COL2 AS M,
       COL1/COL2 AS D
FROM SAMPLE;

SELECT COL1+COL2*COL1 AS R1,
       (COL1+COL2)*COL1 AS R2
FROM SAMPLE;
